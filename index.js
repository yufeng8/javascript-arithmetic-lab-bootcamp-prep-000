function add(x, y) {
  return x+y
}

function subtract(x, y) {
  return x-y
}

function multiply(x, y) {
  return x*y
}

function divide(x, y) {
  return x/y
}

function inc(x) {
  return x++
}

function dec(x) {
  return x--
}

function makeInt(n) {
  return parseInt(n)
}

function preserveDecimal(n) {
  return parseFloat(n)
}
